    private void fillForm() {
        if (product == null) {
            return;
        }

        idTextField.setText(Integer.toString(product.getID()));
        productTextField.setText(product.getProduct());
        priceTextField.setText(Integer.toString(product.getPrice()));

        for (int i = 0; i < categoryList.getModel().getSize(); i++) {
            if (categoryList.getModel().getElementAt(i).equalsIgnoreCase(product.getCategory())) {
                categoryList.setSelectedIndex(i);
                break;
            }
        }

        validateForm();
    }

    private static void connect() {
        if (connection == null) {
            connection = new DBConnection().getDBConn();
        }
    }

    private void validateForm() {
        boolean id = false;
        boolean productName = false;
        boolean price = false;
        boolean category = false;

        id = !idTextField.getText().isEmpty();
        productName = !productTextField.getText().isEmpty();
        price = null != priceTextField.getValue() && isNumber(priceTextField.getValue().toString());
        category = categoryList.getSelectedValue() != null;

        if (id && productName && price && category) {
            confirmButton.setEnabled(true);
        } else {
            confirmButton.setEnabled(false);
        }

    }

    public static boolean isNumber(String target) {
        try {
            Integer.parseInt(target);
            return true;
        } catch (Exception e) {
            return false;
        }
    }

    private void execUpdate(int id, String product, int price, String category) {
        String query = "UPDATE product SET product_name = ?, price = ?, category = ? WHERE product_id = ?";

        try ( PreparedStatement state = connection.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);) {
            state.setString(1, product);
            state.setInt(2, price);
            state.setString(3, category);
            state.setInt(4, id);

            int rowAffected = state.executeUpdate();
            if (rowAffected == 1) {
                infoLabel.setForeground(Color.YELLOW);
                infoLabel.setText("Data UPDATED");
            }

            state.close();
        } catch (Exception ex) {
            infoLabel.setForeground(Color.RED);
            infoLabel.setText("Something went wrong, Failed to execute action");
            Logger.getLogger(this.getName()).log(Level.SEVERE, ex.getMessage(), ex);
        }
    }

